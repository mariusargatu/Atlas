"""SP6 task 2: the pure attribute translation table between `atlas_graph.py`'s informal span
vocabulary (`ok`, `model`, `degradation_mode`, `tool`, `args`, `result`, ...) and the frozen trace
contract's dotted names (`atlas.guard.decision`, `gen_ai.request.model`, `atlas.degradation.mode`,
...). No framework import (no langgraph/mcp/fastapi/opentelemetry/httpx/psycopg): a plain function
over `(name, kind, attrs)`, the exact shape `atlas_graph.py` already passes to `tracer.open(...)`,
mirroring `contract_tools`'s own pure, tested style. `otel_tracer.py` is this module's only caller;
`atlas_graph.py`'s ~20 `tracer.open()` call sites stay completely untouched.

Fails closed, in the adapter's export path, on two independent things:

1. An UNINVENTORIED span: `(name, kind, sorted(attrs.keys()))` was never observed by the committed
   golden inventory (`contracts/trace/span_inventory.json`, regenerated by
   `contract_tools.trace_inventory`, the same "regenerate and assert equality" mechanism
   `contract_tools.mcp_snapshot` already established for MCP tool schemas). A NEW, unreviewed call
   site (a renamed span, an added kwarg) can never silently reach an exported trace with the wrong
   vocabulary; the committed inventory must be regenerated and the diff reviewed first.

   `kind="tool"` spans are the one deliberate exception: the inventory records them under a
   WILDCARD name (`contract_tools.trace_inventory.TOOL_WILDCARD`), never the real, dynamic tool
   name (`get_account_summary`, `search_knowledge`, `change_plan`, ...). Which tools exist is
   `atlas.domain.binding`'s own concern, not a translation one -- every tool span carries exactly
   one of two attribute shapes (`args`+`result` for a read/catalog/knowledge call, `args`+`proposal`
   for a write) and the translation below treats both identically regardless of which tool produced
   them, so per tool name inventory entries would only add churn, never catch a real translation gap.

2. An UNMAPPED attribute key: even on an inventoried span, a key with no entry in `_KEY_RULES`/
   `_PASSTHROUGH_KEYS` below raises rather than silently dropping (or worse, silently forwarding
   under its raw name when a reviewer never actually decided that was fine).

Design decisions this table encodes, all resolved from the OBSERVED call sites in
`atlas_graph.py` (SP6 planning digest section 3, design question 1), never guessed:

- `kind="guard"` + `ok` -> `atlas.guard.decision`, `True`/`False` -> `"allow"`/`"block"`. No guard
  span anywhere in the codebase ever carries a third, step up signal (every one of the ~10 guard
  call sites passes only a bool `ok`); `"step_up"` is therefore never emitted by this table. If a
  future guard grows a real step up signal, add its key here explicitly -- never infer one from
  `ok` alone.
- `degradation_mode` (on the `agent`/`llm` fallback span and the `refusal`/`node` span) ->
  `atlas.degradation.mode` directly: the values already match the contract's 6 member enum
  (`atlas.domain.degradation.escalate`'s own return values), so this is a rename, not a transform.
- `kind="llm"` + `model` -> `gen_ai.request.model` (OTel GenAI semconv naming, D13's "extend, don't
  fork").
- Every other observed key (`reason`, `tool`, `tools`, `intent`, `input`, `hit`, `generic`, `args`,
  `result`, `proposal`, `applied`, `reference`, `output`, `retryable`) has no reserved contract
  counterpart (`testing/harness/contract_tools/loader.py::RESERVED_TRACE_ATTRIBUTES`); the trace
  schema's `attributes` object allows arbitrary additional properties, so these pass through
  UNCHANGED as informal, un namespaced diagnostic attributes -- a deliberate decision, not an
  oversight, so a reviewer can see every key was actually considered.

Two carries this module's docstring is the disclosed home for (SP4 Task 4 / Task 6 ledger lines,
per the SP6 plan and digest):

- `needs_refusal`: `AtlasState`'s own per turn routing signal (`atlas_graph.py`'s `AtlasState`
  TypedDict) is checked against every observed span in the committed inventory -- it never appears
  as a `tracer.open(...)` keyword argument anywhere. It is internal routing state only, never a
  span attribute, and this is the recorded answer to that carry, not an oversight.
- step up (`atlas.guard.decision`'s third enum member): see the guard bullet above. No observed
  guard span carries a step up signal, so it is never emitted; this is resolved from the observed
  inventory, not guessed, per the plan's own instruction.

Stage durations (`atlas.stage.embed_ms`/`retrieve_ms`/`rerank_ms`/`assemble_ms`/`ttft_ms`) are a
SEPARATE, name keyed mechanism (`STAGE_DURATION_ATTRIBUTE` below), computed by `otel_tracer.py`'s
`close(seq)` from its own injected monotonic clock, never derived from this module's inventory
based key table (a `kind="stage"` span carries no informal attributes at open time -- there is
nothing to translate until `close()` supplies a duration).

`atlas.stage.ttft_ms`'s own definition (SP6 task 2 fix round 1, landed after review's Important 1):
wall clock from `chat_app._run_stream`'s own turn start (this generator's first executed line,
before the graph has produced a single event) to the first content bearing SSE event that function
actually yields to the client -- NOT strict OTel GenAI "model first token." This graph fully
buffers its guarded answer and only then streams word chunks (`chat_app.py`'s own `_run_stream`
docstring), so a raw model level first token is never observable from this edge; "turn start to
first streamed content reaching the client" is the most honest boundary available here, and is the
definition any alerting built on this attribute (T5) must treat as authoritative.

Circuit breaker operational state (`CircuitBreaker.state()`) is deliberately ABSENT from every span
attribute in this table. It is live gauge data (the current open/closed/half open state of a
shared resource), not a per turn fact worth attaching to one turn's trace, so it belongs on the
metrics surface, not here: it lands as a Prometheus gauge, deferred to Task 5, and intentionally
has no `RESERVED_TRACE_ATTRIBUTES` slot.

SP6 task 7 (the v1 freeze): the emitter checklist against all 29 `RESERVED_TRACE_ATTRIBUTES` found
10 already emitted (above) and 10 with no real producer anywhere in this codebase, narrowed out of
the freeze via `ADR-029` (machine readable companion:
`contracts/trace/freeze_narrowed.yaml`, `testing/harness/contract_tools/freeze_check.py` reads it as
the single authority). The remaining nine are build wide constants this module already has every
fact it needs to compute -- `BUILD_ATTRIBUTES` below, stamped on every span (not turn only, unlike
`atlas.config.hash`/`atlas.corpus.version`/`atlas.index.build_id`, which stay in `otel_tracer.py`
because they come from `AtlasSettings`, not from anything this module owns).
"""
from __future__ import annotations

