def span_kind_for(name: str, kind: str) -> str:
    """The contract's `span_kind` enum value for one observed `(name, kind)` pair."""
    if kind == "stage":
        return _STAGE_NAME_TO_SPAN_KIND.get(name, "CHAIN")
    return _KIND_TO_SPAN_KIND.get(kind, "CHAIN")


def translate_span(name: str, kind: str, attrs: Mapping[str, Any], *, inventory: frozenset | None = None) -> dict:
    """The full contract shaped span record (`{"name", "span_kind", "attributes"}`,
    `contracts/trace/schema.json`'s own top level shape) for one observed span: translated
    attributes (`translate_attributes`) merged with the two universally required ones
    (`REQUIRED_SPAN_ATTRIBUTES`). A caller supplied attribute never loses to a required one with the
    same key (there are none today; `REQUIRED_SPAN_ATTRIBUTES` intentionally never overlaps
    `_DIRECT_RENAME`/`_PASSTHROUGH_KEYS`'s own targets)."""
    translated = translate_attributes(name, kind, attrs, inventory=inventory)
    return {
        "name": name,
        "span_kind": span_kind_for(name, kind),
        "attributes": {**REQUIRED_SPAN_ATTRIBUTES, **BUILD_ATTRIBUTES, **translated},
    }


