import hmac
import json
from pathlib import Path
from typing import Any, Mapping

# Anchored on this file (not the CWD), the same pattern `AtlasSettings`/`PgvectorRetriever` already
# use, so this module works regardless of where the process was started from.
_REPO_ROOT = Path(__file__).resolve().parents[3]
_INVENTORY_PATH = _REPO_ROOT / "contracts" / "trace" / "span_inventory.json"

# The wildcard name every `kind="tool"` span is inventoried and checked under, regardless of its
# real (dynamic) tool name -- see the module docstring's point 1. Duplicated as a literal (not
# imported from `contract_tools.trace_inventory`, which is harness code backend must never import,
# `testing/tests/test_import_lint.py`'s own boundary) and cross checked by a hermetic test instead.
TOOL_WILDCARD = "*"


# ---- SP8 task 1: the subject pseudonym (D27) -----------------------------------------------------
#
# atlas.subject.pseudonym: an HMAC-SHA256 of customer_id, truncated to the same 16 hex character
# length `_redaction_policy_version`/`index_build_id`/`config_hash` already use elsewhere in this
# repo. Stable per customer (the SAME customer_id always yields the SAME pseudonym) -- the whole
# point of D27's "right to erasure reduces to pseudonym keyed deletes": a real deployment could find
# every trace for one customer by pseudonym without the exported trace ever carrying the raw id.
# Never reversible to customer_id without the key (unlike a plain digest, which anyone who guesses or
# enumerates candidate customer_ids could confirm by re-hashing).
#
# `_PSEUDONYM_KEY` mirrors `backend/atlas/auth.py`'s own `_KEY`: a fixed, local, first party key
# (this reference system's whole corpus is fabricated demo data, CLAUDE.md's own "known, intentional
# simplifications"), never a real secret manager -- the same posture `auth.py`'s own comment already
# documents for its signing key ("not a production secret").
_PSEUDONYM_KEY = b"atlas-local-dev-pseudonym-key-not-a-production-secret-v1"
_PSEUDONYM_HEX_LEN = 16


def pseudonym_for(customer_id: str) -> str:
    """HMAC-SHA256 of `customer_id`, truncated to `_PSEUDONYM_HEX_LEN` hex characters. The ONE
    computation `atlas.subject.pseudonym` values ever come from, live or hermetic -- see the module
    level comment above for why this is HMAC, not a plain digest. `str(customer_id)` (never a bare
    `.encode`): `freeze_check.hermetic_emitted_attributes`/`redaction.emitted_gen_ai_attributes` walk
    the committed span inventory with a placeholder `True` standing in for every real value (they
    only care which ATTRIBUTE NAMES a shape translates to, never the values), so this must tolerate
    a non string placeholder exactly like every other rule in this table already does."""
    mac = hmac.new(_PSEUDONYM_KEY, str(customer_id).encode("utf-8"), hashlib.sha256)
    return mac.hexdigest()[:_PSEUDONYM_HEX_LEN]


class TraceTranslationError(RuntimeError):
    """Base class for a fail closed translation refusal (SP6 task 2)."""


class UninventoriedSpanError(TraceTranslationError):
    """A `(name, kind, attrs)` shape the committed golden inventory never observed."""


class UnmappedAttributeError(TraceTranslationError):
    """An inventoried span carries a key with no translation rule below."""


def _load_inventory() -> frozenset[tuple[str, str, frozenset[str]]]:
    if not _INVENTORY_PATH.is_file():
        # Fail closed before ANY translation happens, the same "fail before the network call"
        # discipline `PgvectorRetriever._load_fingerprint` uses: a missing inventory is a build
        # problem, never a silent "nothing is inventoried" pass through.
        raise TraceTranslationError(
            f"no committed span inventory at {_INVENTORY_PATH}; generate one with "
            "`uv run python -m contract_tools.trace_inventory --write` before this adapter can export."
        )
    raw = json.loads(_INVENTORY_PATH.read_text())
    return frozenset((entry["name"], entry["kind"], frozenset(entry["attrs"])) for entry in raw)


# Loaded once at import time: the inventory is a committed, reviewed file, not something that
# changes mid process. A test that wants a DIFFERENT inventory (proving the fail closed behavior
# itself) calls `translate_span` with an explicit `inventory=` override instead of touching this.
_INVENTORY = _load_inventory()

# ---- the key translation table -----------------------------------------------------------------

# key -> dotted contract name, verbatim value (no transform needed: the value already matches the
# contract's own enum/shape at the source, see the module docstring).
_DIRECT_RENAME: dict[str, str] = {
    "degradation_mode": "atlas.degradation.mode",
}

# Keys with no reserved contract counterpart: pass through unchanged (module docstring, point 2).
_PASSTHROUGH_KEYS: frozenset[str] = frozenset({
    "input", "intent", "hit", "generic", "reason", "tool", "tools",
    "args", "result", "proposal", "applied", "reference", "output", "retryable",
})


def _inventory_key(name: str, kind: str, attrs: Mapping[str, Any]) -> tuple[str, str, frozenset[str]]:
    checked_name = TOOL_WILDCARD if kind == "tool" else name
    return (checked_name, kind, frozenset(attrs.keys()))


def _translate_attr(kind: str, key: str, value: Any) -> tuple[str, Any]:
    if key == "ok" and kind == "guard":
        return "atlas.guard.decision", ("allow" if value else "block")
    if key == "model" and kind == "llm":
        return "gen_ai.request.model", value
    # SP8 task 1: the subject pseudonym. `customer_id` is threaded through atlas_graph.py's "turn"
    # tracer.open call site as a new kwarg (per ADR-029), sourced from the session/bearer identity,
    # never a tool argument and never the model (CLAUDE.md's own rule) -- this is the ONE place the
    # raw id is ever seen by this module, and it is HMAC'd (`pseudonym_for`, never passed through
    # verbatim) before it becomes an attribute value.
    if key == "customer_id" and kind == "turn":
        return "atlas.subject.pseudonym", pseudonym_for(value)
    # SP8 task 1: the judge's own span (`judge.emission.emit_verdict`'s informal kwarg names). A
    # straight rename, no value transform: `judge_id` is already the contract's fingerprint digest,
    # `rubric_version` is already the rubric's own version string, and `verdict` is already the WIRE
    # vocabulary (`judge.llm_judge.translate_verdict`'s own output) by the time it reaches here --
    # this table never translates the judge's PASS/FAIL prompt vocabulary itself.
    if kind == "judge" and key == "judge_id":
        return "atlas.judge.id", value
    if kind == "judge" and key == "rubric_version":
        return "atlas.judge.rubric_version", value
    if kind == "judge" and key == "verdict":
        return "atlas.judge.verdict", value
    # SP9 task 5: the usage accounting trio (ADR-029's cost amendment). `matrix/cost_emission.py`'s
    # own informal kwarg names, on the SAME kind="llm" span shape `agent`'s own `model` key already
    # uses -- scoped to kind=="llm" only (never a blanket rename), so the same key on a different
    # kind still fails closed rather than silently aliasing.
    if kind == "llm" and key == "input_tokens":
        return "atlas.cost.input_tokens", value
    if kind == "llm" and key == "output_tokens":
        return "atlas.cost.output_tokens", value
    if kind == "llm" and key == "usd":
        return "atlas.cost.usd", value
    if key in _DIRECT_RENAME:
        return _DIRECT_RENAME[key], value
    if key in _PASSTHROUGH_KEYS:
        return key, value
    raise UnmappedAttributeError(
        f"span (kind={kind!r}) attribute {key!r} has no translation rule in "
        "backend/atlas/adapters/trace_translation.py's key table; add one (a dotted rename or an "
        "explicit passthrough decision) before this can export."
    )


def translate_attributes(
    name: str, kind: str, attrs: Mapping[str, Any], *, inventory: frozenset | None = None,
) -> dict[str, Any]:
    """Translate one observed span's informal `attrs` into the frozen contract's dotted names.
    Fails closed (raises `UninventoriedSpanError`/`UnmappedAttributeError`) rather than silently
    dropping or silently forwarding anything not already reviewed. `inventory` is test only (the
    fail closed behavior itself needs a controllable inventory); production callers never pass it,
    so they always check against the one committed file loaded at import time."""
    allowed = _INVENTORY if inventory is None else inventory
    key = _inventory_key(name, kind, attrs)
    if key not in allowed:
        raise UninventoriedSpanError(
            f"span (name={name!r}, kind={kind!r}, attrs={sorted(attrs.keys())}) was never observed "
            "by the committed span inventory (contracts/trace/span_inventory.json). Regenerate it "
            "(`uv run python -m contract_tools.trace_inventory --write`) after reviewing this new "
            "call site's vocabulary, then commit the diff."
        )
    return dict(_translate_attr(kind, k, v) for k, v in attrs.items())


