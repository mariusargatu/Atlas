_CONTENT_CAPTURED = False

# `atlas.privacy.redaction_policy_version` (SP6 task 7 review fix round 1, Important 1 -- the
# versioning trap): this used to be a literal copy of `_CONTRACT_TRACE_VERSION`. That is a real gap,
# not just an identity: `contract_tools.redaction.allowed_attributes()` unions `RESERVED_TRACE_
# ATTRIBUTES` (tied to the trace schema) with the `gen_ai.*` keys THIS table actually emits (tied to
# nothing but this table itself, entirely independent of `contracts/trace/schema.json`) and
# `openinference.span.kind`. A future PR adding a new `gen_ai.*` mapping below changes the REAL
# redaction allowlist with no bump obligation attached to the trace schema at all, and the copied
# version string would never move to say so.
#
# The fix is CONTENT ADDRESSING, the same "hash the canonical content, truncate to a short hex
# prefix" philosophy `chunk_id`/`index_build_id`/`config_hash` already use elsewhere in this repo:
# a short sha256 prefix over the allowlist's own canonical, sorted key list. This module cannot
# import `contract_tools.redaction.redaction_policy_version` to get that value directly (a backend
# module may never import testing harness code, `test_import_lint.py`'s own boundary, and doing so
# would also be a genuine circular import: `contract_tools.redaction` itself imports THIS module to
# derive its own gen_ai.* category). Instead, the three helpers below independently recompute the
# SAME three allowlist inputs, entirely from committed files and this module's own translation
# table, and hash them the SAME way -- `testing/tests/test_trace_translation.py` cross checks the
# result against `contract_tools.redaction.redaction_policy_version()`'s own output, the same
# "two independent implementations over the same underlying data, cross checked by a test, never a
# Python import" discipline `_contract_family_version`/`contract_tools.loader.contract_versions()`
# already established for the other three contract family versions.
_POLICY_VERSION_HEX_LEN = 16
_OPENINFERENCE_SPAN_KIND_ATTRIBUTE = "openinference.span.kind"


def _reserved_trace_attributes_from_schema() -> tuple[str, ...]:
    """The trace schema's OWN declared attribute property names, read directly from the committed
    file (never a `contract_tools.loader.RESERVED_TRACE_ATTRIBUTES` import -- see the comment
    above). `testing/harness/contract_tools/loader.py`'s own 29 name literal is the SAME set, in the
    SAME order; `test_reserved_trace_attributes_from_schema_matches_the_loaders_own_list` cross
    checks the two stay in agreement rather than assuming it by construction."""
    schema = json.loads((_REPO_ROOT / "contracts" / "trace" / "schema.json").read_text())
    return tuple(schema["properties"]["attributes"]["properties"].keys())


def _emitted_gen_ai_attributes() -> frozenset[str]:
    """Every `gen_ai.*` key `translate_attributes` (above) actually produces when walking the
    committed golden span inventory -- the exact SAME derivation `contract_tools.redaction.
    emitted_gen_ai_attributes()` performs via an import of THIS module's `translate_attributes`,
    performed here as a local call instead so this module never has to import back out to the
    harness that already imports it."""
    entries = json.loads(_INVENTORY_PATH.read_text())
    keys: set[str] = set()
    for entry in entries:
        attrs = {key: True for key in entry["attrs"]}
        translated = translate_attributes(entry["name"], entry["kind"], attrs)
        keys.update(key for key in translated if key.startswith("gen_ai."))
    return frozenset(keys)


def _redaction_policy_version() -> str:
    """The SAME computation `contract_tools.redaction.redaction_policy_version()` performs over
    `allowed_attributes()`, independently recomputed here from this module's own three sources
    (never an import; see the block comment above): a short sha256 prefix over the canonical,
    sorted union of the reserved names, the emitted gen_ai.* keys, and the openinference span kind
    constant, newline joined -- identical inputs, identical algorithm, so the two values agree
    without ever sharing code."""
    allowed = sorted(
        set(_reserved_trace_attributes_from_schema())
        | _emitted_gen_ai_attributes()
        | {_OPENINFERENCE_SPAN_KIND_ATTRIBUTE}
    )
    canonical = "\n".join(allowed)
    return hashlib.sha256(canonical.encode("utf-8")).hexdigest()[:_POLICY_VERSION_HEX_LEN]


